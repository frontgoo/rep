package org.frontgoo.scrap.ali.model;

public class ColorSizeEntity {

	private String id;
	private String parentId;
	private String canBookCount;
	private Integer saleCount;
	private String specId;
	
	/**
	 * @return the id
	 */
	public String getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(String id) {
		this.id = id;
	}
	/**
	 * @return the parentId
	 */
	public String getParentId() {
		return parentId;
	}
	/**
	 * @param parentId the parentId to set
	 */
	public void setParentId(String parentId) {
		this.parentId = parentId;
	}
	/**
	 * @return the canBookCount
	 */
	public String getCanBookCount() {
		return canBookCount;
	}
	/**
	 * @param canBookCount the canBookCount to set
	 */
	public void setCanBookCount(String canBookCount) {
		this.canBookCount = canBookCount;
	}
	/**
	 * @return the saleCount
	 */
	public Integer getSaleCount() {
		return saleCount;
	}
	/**
	 * @param saleCount the saleCount to set
	 */
	public void setSaleCount(Integer saleCount) {
		this.saleCount = saleCount;
	}
	/**
	 * @return the specId
	 */
	public String getSpecId() {
		return specId;
	}
	/**
	 * @param specId the specId to set
	 */
	public void setSpecId(String specId) {
		this.specId = specId;
	}
	
}
